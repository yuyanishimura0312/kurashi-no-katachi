def get_published_eps():
    return {ep[0] for ep in ROADMAP if ep[6] == 'published'}


def get_part_for_ep(ep_num: str) -> str:
    n = int(ep_num)
    if n <= 5: return 'prologue'
    if n <= 23: return 'part-i'
    if n <= 41: return 'part-ii'
    if n <= 59: return 'part-iii'
    if n <= 77: return 'part-iv'
    if n <= 95: return 'part-v'
    return 'final'
